let x = Questions['generalQuestions'];     // x are the selection of quiz (example: general knowledge)
let z = 0;                                 // z current question (example: 6 of 10)
let counter = 0;                           // number of right answer counter

function init() {
    showStartscreen();
}

//______________________________  show and hide a body ___________________________________

function hideAll() {
    document.getElementById('startScreen').classList.add('d-none');
    document.getElementById('questionScreen').classList.add('d-none');
    document.getElementById('endScreen').classList.add('d-none');
}

function showStartscreen() {
    hideAll();
    document.getElementById('startScreen').classList.remove('d-none');
}

function showQuestionscreen() {
    hideAll();
    document.getElementById('questionScreen').classList.remove('d-none');
}

function showEndscreen() {
    document.getElementById('right-answer-count').innerHTML = counter;
    hideAll();
    document.getElementById('endScreen').classList.remove('d-none');
}

//_____________________________ load a quiz _________________________________________

function startQuiz(i) {
    loadNewX(i);
    loadTheQuestionCountdowns();
    showQuestionscreen();
    showQuestion();
    aktivateAnswerButtons();
}

function loadNewX(i) {
    x = Questions[i];
}

function loadTheQuestionCountdowns() {
    document.getElementById('question-count').innerHTML = x.length;
    document.getElementById('question-count-endscreen').innerHTML = x.length;
}

function showQuestion() {
    if (z == x.length) {
        showEndscreen();
    } else {
        updateQuestion();
        aktivateAnswerButtons();
    }
}

function updateQuestion() {
    updateProgressBar();
    document.getElementById('questiontext').innerHTML = x[z]['question'];
    document.getElementById('answer1').innerHTML = x[z]['answer1'];
    document.getElementById('answer2').innerHTML = x[z]['answer2'];
    document.getElementById('answer3').innerHTML = x[z]['answer3'];
    document.getElementById('answer4').innerHTML = x[z]['answer4'];
    document.getElementById('number-of-question').innerHTML = z + 1;
}

function updateProgressBar() {
    let percent = Math.round(z / x.length * 100);
    document.getElementById('progress-bar').style.width = `${percent}%`;
    document.getElementById('progress-bar').innerHTML = `${percent} %`;
}

function nextQuestion() {
    z++;
    deaktivateNextQuestionButton();
    resetAnswerButtons();
    showQuestion();
}

// _________________ return right or wrong _________________________________________________

function returnRight(i) {
    document.getElementById(`answer${i}`).parentNode.classList.add('bg-success');
    AUDIO_RIGHT.play();
    counter++;
}

function returnWrong(i) {
    document.getElementById(`answer${i}`).parentNode.classList.add('bg-danger');
    document.getElementById(`answer${x[z]['right_answer']}`).parentNode.classList.add('bg-success');
    AUDIO_WRONG.play();
}

//_________________ buttons aktivate and deaktivate the next question button ______________

function deaktivateNextQuestionButton() {
    document.getElementById('next-button').disabled = true;
}

function aktivateNextQuestionButton() {
    document.getElementById('next-button').disabled = false;
}


//_________________________ answer ________________________________________________________
function answer(y) {
    if (y == x[z]['right_answer']) {
        returnRight(y);
    } else {
        returnWrong(y);
    }
    deaktivateAnswerButtons();
    aktivateNextQuestionButton();
}


//_______________________________ answer buttons __________________________________________


function resetAnswerButtons() {
    for (let i = 1; i < 5; i++) {
        document.getElementById(`answer${i}`).parentNode.classList.remove('bg-danger');
        document.getElementById(`answer${i}`).parentNode.classList.remove('bg-success');
    }
}

function aktivateAnswerButtons() {
    document.getElementById('overlay').classList.remove('z1');
}

function deaktivateAnswerButtons() {
    document.getElementById('overlay').classList.add('z1');
}
//_____________________________ newstart __________________________________________________

function newstart() {
    z = 0;
    counter = 0;
    init();
}

// ____________________________________  audio  ___________________________________________

let AUDIO_RIGHT = new Audio('audio/right.mp3');
let AUDIO_WRONG = new Audio('audio/wrong.mp3');
